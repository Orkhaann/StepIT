use [AcademyDB]

------------------------------------------------------------------------- 1 -------------------------------------------------------------------------

SELECT
    REVERSE(Id) AS ReversedId,
    REVERSE(Financing) AS ReversedFinancing,
    REVERSE(Name) AS ReversedName
FROM
    Departments;

--------------------------------------------------------------------------- 2 -------------------------------------------------------------------------

SELECT
    Name AS [Group Name],
    Rating AS [Group Rating]
FROM
    Groups;

------------------------------------------------------------------------- 3 -------------------------------------------------------------------------

SELECT
    Surname,
    ((Salary / Premium) * 100)
FROM
    Teachers;

------------------------------------------------------------------------- 4 -------------------------------------------------------------------------

SELECT
    CONCAT('The dean of faculty ', Name, ' is ', Dean) AS FacultyInformation
FROM
    Faculties;

------------------------------------------------------------------------- 5 -------------------------------------------------------------------------

SELECT
    Name
FROM
    Teachers
WHERE
    IsProfessor = 1
    AND Salary > 1050;

------------------------------------------------------------------------- 6 -------------------------------------------------------------------------

SELECT
    Name
FROM
    Departments
WHERE
    Financing < 11000 OR Financing > 25000;

----------------------------------------------------------------------- 7 -------------------------------------------------------------------------

SELECT
    Name
FROM
    Faculties
WHERE
    Name != 'Computer Science';

----------------------------------------------------------------------- 8 -------------------------------------------------------------------------

SELECT
    Name,
    Position
FROM
    Teachers
WHERE
    IsProfessor != 1;

----------------------------------------------------------------------- 9 -------------------------------------------------------------------------

SELECT
    Surname,
    Position,
    Salary,
    Premium
FROM
    Teachers
WHERE
    IsAssistant = 1
    AND Premium BETWEEN 160 AND 550;

----------------------------------------------------------------------- 10 -------------------------------------------------------------------------

SELECT
    Surname,
    Salary
FROM
    Teachers
WHERE
    IsAssistant = 1;

----------------------------------------------------------------------- 11 -------------------------------------------------------------------------

SELECT
    Surname,
    Position
FROM
    Teachers
WHERE
    EmploymentDate < '2000-01-01';

----------------------------------------------------------------------- 12 -------------------------------------------------------------------------

SELECT
    Name
FROM
    Departments
WHERE
    Name <= 'Software Development Department'
ORDER BY
    Name;

----------------------------------------------------------------------- 13 -------------------------------------------------------------------------

SELECT
    Name
FROM
    Teachers
WHERE
    IsAssistant = 1
    AND (Salary + Premium) <= 1200;

----------------------------------------------------------------------- 14 -------------------------------------------------------------------------

SELECT
    Name
FROM
    Groups
WHERE
    Year = 5
    AND Rating BETWEEN 2 AND 4;

----------------------------------------------------------------------- 15 -------------------------------------------------------------------------

SELECT
    Name
FROM
    Teachers
WHERE
    IsAssistant = 1
    AND (Salary < 550 OR Premium < 200);